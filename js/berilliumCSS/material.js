function arrow_ios_back(){
const elements = document.querySelectorAll('[class *= "|mat arrow back ios|"]');

elements.forEach(element => {
    element.style.webkitMaskImage = 'url(\'../../../icons/material/arrow_back_ios.svg\')';
    element.style.webkitMaskSize = 'contain';
    element.style.webkitMaskRepeat = 'no-repeat';
    element.style.maskImage = 'url(\'../../../icons/material/arrow_back_ios.svg\')';
    element.style.maskSize = 'contain';
    element.style.maskRepeat = 'no-repeat';
});
}

